#Elif Statement
x = 33
y = 33
if y > x:
    print("y is greater than x")
elif x == y:
    print("x and x are equal")



#Testing multiple conditions:
score = 75

if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
elif score >= 60:
    print("Grade: D")


